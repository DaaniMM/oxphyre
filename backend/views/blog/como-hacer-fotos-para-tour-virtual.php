      <h1 class="mvp-title">Cómo hacer fotos para un tour virtual con el móvil y que tu negocio se vea mejor</h1>
      <p class="mvp-lead">Una buena visita empieza antes de subir imágenes. Esta guía te ayuda a preparar el local, elegir luz y decidir el encuadre para que un cliente entienda el espacio sin estar allí.</p>
    </header>

    <section class="mvp-section">
      <div class="mvp-container mvp-post-layout">
        <article class="mvp-post-body">
          <p>Cuando un restaurante pequeño enseña el comedor con mesas torcidas y una ventana quemada por el sol, la sensación baja aunque el local sea agradable. Lo mismo pasa en una barbería si aparecen cables, productos desordenados o sillas a medio mover. Hacer fotos para tour virtual no va de decorar de más: va de enseñar el sitio como te gustaría que lo encontrara un cliente.</p>

          <h2>Prepara el local antes de hacer fotos</h2>
          <p>Dedica diez minutos a retirar objetos temporales: bolsas, cajas de proveedores, cubos de limpieza, papeles en la barra o carteles viejos. Si hay mesas, alinea sillas y manteles. Si es una tienda, deja el pasillo despejado y evita que el mostrador parezca zona de almacén.</p>
          <ul>
            <li>Abre persianas si la luz exterior es suave.</li>
            <li>Enciende las luces que usarías con clientes dentro.</li>
            <li>Retira datos sensibles: albaranes, pantallas con pedidos o notas internas.</li>
            <li>Revisa espejos y cristales si tienes peluquería, clínica o escaparate.</li>
          </ul>

          <h2>Luz: clara, estable y sin extremos</h2>
          <p>La luz mixta puede arruinar una foto sencilla. Evita fotografiar una sala oscura con una ventana muy brillante al fondo. En un bar, por ejemplo, suele funcionar mejor hacer la foto cuando la luz de la calle no entra directa y las lámparas interiores están encendidas.</p>

          <h2>Encuadre: fotografía desde donde estaría una persona</h2>
          <p>Coloca el móvil aproximadamente a la altura del pecho o de los ojos. En una tienda, sitúate en el punto desde el que un cliente entiende el pasillo principal. En un restaurante, empieza desde la entrada o desde un lateral que muestre mesas y circulación.</p>

          <h2>Orden visual: menos cosas, más claridad</h2>
          <p>El tour debe ayudar a orientarse. Si una zona tiene demasiados estímulos, el cliente no sabe qué mirar. En una barbería, una foto limpia de los sillones comunica mejor que una foto llena de productos, cables y capas colgadas.</p>

          <h2>Fotos horizontales y panorámicas</h2>
          <p>El formato horizontal suele funcionar mejor para enseñar espacios. Si tu móvil permite panorámica, úsala con movimiento lento, sin girar el cuerpo demasiado rápido y sin capturar personas reconocibles sin permiso. No hace falta prometer cobertura total: una panorámica parcial bien hecha puede orientar mejor que varias fotos sueltas.</p>
          <h3>Panorámica con iPhone o Android</h3>
          <p>Activa el modo panorámica y mueve el móvil despacio, manteniendo una altura estable desde el inicio hasta el final. Al terminar, revisa si las líneas de paredes, mesas o puertas salen torcidas; si la imagen queda deformada, repítela antes de subirla.</p>

          <h2>Mejor / Evitar</h2>
          <table class="mvp-table">
            <thead>
              <tr><th>Mejor</th><th>Evitar</th></tr>
            </thead>
            <tbody>
              <tr><td>Fotos horizontales desde puntos naturales del recorrido.</td><td>Fotos verticales recortadas que no enseñan el espacio.</td></tr>
              <tr><td>Luz homogénea y local preparado como en horario de atención.</td><td>Ventanas quemadas, zonas negras o luces apagadas.</td></tr>
              <tr><td>Mesas, sillas y productos ordenados.</td><td>Cajas, papeles, cables y objetos temporales en primer plano.</td></tr>
              <tr><td>Panorámica lenta si el móvil la genera con estabilidad.</td><td>Mover el móvil rápido y deformar paredes o líneas.</td></tr>
            </tbody>
          </table>

          <h2>Errores comunes</h2>
          <ul>
            <li>Hacer la foto justo al cerrar, cuando el local ya está a medio recoger.</li>
            <li>Enseñar zonas irrelevantes como almacenes abiertos o pasillos técnicos.</li>
            <li>Usar zoom digital, que reduce nitidez y hace más difícil leer el espacio.</li>
            <li>Enviar la foto por WhatsApp antes de subirla. Es uno de los errores más habituales: comprime la imagen, reduce nitidez y puede hacer que el tour se vea peor aunque el local esté bien fotografiado.</li>
          </ul>

          <h2>Checklist final</h2>
          <ol>
            <li>Elige 3-5 zonas que expliquen el recorrido real.</li>
            <li>Ordena lo que aparece en cada zona.</li>
            <li>Haz fotos horizontales con el móvil estable.</li>
            <li>Revisa luz, reflejos y datos sensibles.</li>
            <li>Sube las imágenes y prueba el tour desde el móvil.</li>
          </ol>

