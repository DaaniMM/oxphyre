        <h2>QR en escaparate</h2>
        <p>Funciona bien en restaurantes, tiendas y estudios con horario limitado. La frase debe ser concreta: "Escanea y mira el local por dentro" o "Conoce la terraza antes de reservar". Evita textos vagos que no expliquen qué verá la persona.</p>

        <h2>QR en mesas</h2>
        <p>En un restaurante, puede acompañar la carta si quieres mostrar terraza, comedor para grupos o una sala interior. No lo mezcles con demasiadas acciones; si el QR lleva al tour, que el texto lo diga.</p>

        <h2>QR en tarjetas</h2>
        <p>Una barbería puede entregar una tarjeta con el QR del local para que el cliente enseñe el sitio a otra persona. Una tienda puede usarlo en bolsas o flyers para que alguien recuerde la distribución y vuelva con una idea clara.</p>

        <h2>QR en redes y cartelería</h2>
        <p>En redes, el enlace suele ser más práctico que la imagen del QR. En cartelería física, el QR gana valor porque evita escribir una URL larga. Prueba el cartel con un móvil real antes de imprimir muchas copias.</p>

        <h2>Mini casos</h2>
        <div class="mvp-grid-3">
          <article class="mvp-case">
            <h3>Restaurante</h3>
            <p>QR en escaparate con acceso al recorrido de entrada, comedor y terraza. Complementa la página de <a href="/tour-virtual-para-restaurantes">tour virtual para restaurantes</a>.</p>
          </article>
          <article class="mvp-case">
            <h3>Barbería</h3>
            <p>QR en tarjeta de cita para enseñar sillones, zona de espera y estilo del local a nuevos clientes.</p>
          </article>
          <article class="mvp-case">
            <h3>Tienda</h3>
            <p>QR en el escaparate para mostrar secciones interiores cuando hay productos que no se ven desde fuera.</p>
          </article>
        </div>

        <h2>Consejos para colocar el QR</h2>
        <ul>
          <li>Incluye una frase que explique el beneficio: mirar el local, ver la terraza o conocer la tienda.</li>
          <li>Colócalo a una altura cómoda y con contraste suficiente.</li>
          <li>Evita superficies curvas, brillantes o muy oscuras.</li>
          <li>Prueba el escaneo desde Android y iPhone antes de imprimir.</li>
          <li>Comprueba que el tour se entiende desde móvil.</li>
        </ul>

