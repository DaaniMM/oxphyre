        <h2>Dónde colocar el QR de tu tour virtual</h2>
        <p>El lugar cambia la intención. Un QR en el escaparate habla con quien pasa por la calle. Un QR en mesa ayuda a descubrir otras zonas del restaurante. En una tarjeta, sirve para que alguien recuerde el local después de una conversación.</p>
        <div class="mvp-qr-locations">
          <article class="mvp-card"><h3>Escaparate</h3><p>Útil cuando el local está cerrado o la persona duda si entrar.</p></article>
          <article class="mvp-card"><h3>Mesa</h3><p>Sirve para enseñar terraza, comedor privado o zonas que el cliente no ve desde su asiento.</p></article>
          <article class="mvp-card"><h3>Tarjeta</h3><p>Convierte una tarjeta física en una visita visual al negocio.</p></article>
          <article class="mvp-card"><h3>Redes</h3><p>Puedes usar el enlace del tour junto al QR en historias, destacados o publicaciones.</p></article>
          <article class="mvp-card"><h3>Cartel</h3><p>En ferias, eventos o recepción, permite enseñar el local sin abrir una galería larga.</p></article>
